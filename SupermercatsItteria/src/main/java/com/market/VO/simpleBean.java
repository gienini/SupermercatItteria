package com.market.VO;

public class simpleBean { 
    private String prova;

    public simpleBean() {
        super();
    }

    public simpleBean(String prova) {
        super();
        this.prova = prova;
    }

    public String getProva() {
        return prova;
    }

    public void setProva(String prova) {
        this.prova = prova;
    }

}
